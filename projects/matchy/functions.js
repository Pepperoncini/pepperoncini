//////////////////////////////////////////////////////////////////////
// Step 1 - Search ///////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////
function search(animals, name) {
    for (let animal of animals) {
    if (animal.name === name) {
        return animal;
        }
    }
    return null;
}


//////////////////////////////////////////////////////////////////////
// Step 2 - Replace //////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////
function replace(animals, name, replacement) {
   if(search(animals, name)) {
animals[animals.indexOf(search(animals, name))] = replacement;
   }
}


//////////////////////////////////////////////////////////////////////
// Step 3 - Remove ///////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////
function remove(animals, name) {
    if(search(animals, name)) {
    animals.splice(animals[animals.indexOf(search(animals, name))], 1);
    }
 }


